def pc_to_networkx(adj, node_names):
    """Convert PC adjacency matrix to NetworkX DiGraph (directed + undirected edges)."""
    G_nx = nx.DiGraph()
    G_nx.add_nodes_from(node_names)
    for i in range(adj.shape[0]):
        for j in range(i+1, adj.shape[1]):
            if   adj[i,j]==1 and adj[j,i]==-1: G_nx.add_edge(node_names[i], node_names[j])
            elif adj[j,i]==1 and adj[i,j]==-1: G_nx.add_edge(node_names[j], node_names[i])
            elif adj[i,j]==-1 and adj[j,i]==-1:
                G_nx.add_edge(node_names[i], node_names[j])
                G_nx.add_edge(node_names[j], node_names[i])

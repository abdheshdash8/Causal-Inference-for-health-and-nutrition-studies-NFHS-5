        plot_data = []
        plot_labels = []

        if dr_cate is not None:
            plot_data.append(dr_cate)
            plot_labels.append("DR Learner")
        if x_cate is not None:
            plot_data.append(x_cate)
            plot_labels.append("X-Learner")

        parts = ax.violinplot(plot_data, positions=range(len(plot_data)), showmedians=True)


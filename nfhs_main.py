"""## 6. Estimator 2 — Doubly Robust (DR) Learner via EconML
The **DR Learner** (also called AIPW — Augmented Inverse Probability Weighted) is *doubly robust*:
it remains consistent if **either** the propensity model **or** the outcome model is correctly specified.
It also yields heterogeneous treatment effect estimates (CATE) at the individual state level.

We use EconML's `LinearDRLearner` with gradient-boosted outcome models.

"""

def estimate_dr_learner(pathway, df):
    """
    Estimate ATE + CATE using EconML LinearDRLearner (Doubly Robust).
    """
    try:
        _, T_bin, _, Y, X_conf, _ = prepare_pathway_data(pathway, df)

        scaler = StandardScaler()
        X_scaled = scaler.fit_transform(X_conf)

        dr = LinearDRLearner(
            model_propensity=LogisticRegression(max_iter=1000, C=1.0),
            model_regression=GradientBoostingRegressor(n_estimators=100, max_depth=3, random_state=42),
            cv=3,
            random_state=42
        )
        dr.fit(Y, T_bin, X=X_scaled, W=None)

        ate = dr.ate(X_scaled)
        ate_interval = dr.ate_interval(X_scaled, alpha=0.05)
        ci_lo, ci_hi = ate_interval[0], ate_interval[1]
        se = (ci_hi - ci_lo) / (2 * 1.96)
        z = ate / se if se > 0 else np.nan
        p_val = 2 * (1 - stats.norm.cdf(abs(z))) if not np.isnan(z) else np.nan

        # CATE per state
        cate = dr.effect(X_scaled)

        return {
            "ATE": ate, "SE": se, "CI_lo": ci_lo, "CI_hi": ci_hi,
            "p_value": p_val, "CATE": cate
        }

    except Exception as e:
        print(f"    DR Learner failed: {e}")
        return {"ATE": np.nan, "SE": np.nan, "CI_lo": np.nan, "CI_hi": np.nan,
                "p_value": np.nan, "CATE": None}


print("Running Doubly Robust Learner (EconML) on all pathways …\n")

import itertools

def partial_corr(df, x, y, controls, min_n=5):
    """Partial correlation of x,y after linearly controlling for `controls`.

    Fix: deduplicate the column list before selecting from df.
    When x or y is also in controls (e.g. literacy is both a
    control and a treatment), the naive [x,y]+controls list creates
    duplicate column names.  pandas then returns a 2-column DataFrame
    for temp[x] instead of a Series, making pearsonr raise a shape
    mismatch ValueError.  We deduplicate while preserving order and
    use .values throughout to stay in numpy and avoid index-alignment
    broadcasting on bootstrap resamples.
    """
    # Deduplicate while preserving order
    seen, unique_cols = set(), []
    for c in [x, y] + controls:
        if c not in seen:
            seen.add(c); unique_cols.append(c)

    temp = df[unique_cols].apply(pd.to_numeric, errors="coerce").dropna()
    if len(temp) < min_n:
        return np.nan

    # Which controls are still present after deduplication
    ctrl_cols = [c for c in controls if c in temp.columns
                 and c != x and c != y]  # exclude x/y if they equal a control
    if not ctrl_cols:           # nothing to partial out — return raw corr
        rx = temp[x].values
        ry = temp[y].values
    else:
        X_ctrl = temp[ctrl_cols].values          # shape (n, k), always 2D
        def resid(col):
            y_vals = temp[col].values             # shape (n,), always 1D
            reg = LinearRegression().fit(X_ctrl, y_vals)
            return y_vals - reg.predict(X_ctrl).ravel()  # shape (n,)
        rx = resid(x)
        ry = resid(y)

    if rx.std() == 0 or ry.std() == 0:
        return np.nan
    return pearsonr(rx, ry)[0]


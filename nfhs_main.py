def bootstrap_full(df, cols, n_boot=N_BOOT):
    """
    Returns three dicts: (fwd_conf, rev_conf, bidir_conf).
    fwd_conf[(A,B)]   = fraction of boots where A→B (strictly directed).
    rev_conf[(A,B)]   = fraction of boots where B→A (reverse directed).
    bidir_conf[(A,B)] = fraction of boots where A—B (undirected).
    """
    fwd_counts   = defaultdict(int)
    rev_counts   = defaultdict(int)
    bidir_counts = defaultdict(int)
    data = df[cols].dropna()
    for _ in range(n_boot):
        sample = data.sample(frac=1, replace=True)
        cg     = pc(sample.values)          # causal-learn PC
        adj    = cg.G.graph
        for i in range(adj.shape[0]):
            for j in range(adj.shape[1]):
                if adj[i,j]==1 and adj[j,i]==-1:
                    fwd_counts[(cols[i], cols[j])] += 1
                elif adj[j,i]==1 and adj[i,j]==-1:
                    rev_counts[(cols[i], cols[j])] += 1
                elif adj[i,j]==-1 and adj[j,i]==-1 and i < j:
                    bidir_counts[(cols[i], cols[j])] += 1
    all_pairs = set(fwd_counts) | set(rev_counts) | set(bidir_counts)
    return (
        {e: fwd_counts[e] / n_boot  for e in all_pairs},
        {e: rev_counts[e] / n_boot  for e in all_pairs},
        {e: bidir_counts[e] / n_boot for e in all_pairs},
    )

def partial_corr_residual(df, x_col, y_col, controls):
    """Partial correlation of x_col and y_col after partialling out controls.

    Root-cause fix (ValueError: shape mismatch in pearsonr):
    When x_col or y_col is also listed in controls (e.g. literacy is
    both a confounder and the treatment), the column list
    [x_col, y_col] + controls contains duplicates.  pandas returns a
    2-column DataFrame for sub[x_col] instead of a 1-column Series,
    so sub[x_col].values has shape (n, 2) not (n,), causing pearsonr
    to raise a broadcastability ValueError.

    Fix: build a deduplicated column list before calling df[...],
    and exclude x_col/y_col from the regressor set when they overlap
    with controls.
    """
    # Deduplicate column list while preserving order
    seen, unique_cols = set(), []
    for c in [x_col, y_col] + controls:
        if c not in seen:
            seen.add(c); unique_cols.append(c)

    sub = df[unique_cols].dropna()
    if len(sub) < 8:
        return np.nan

    # Controls used in regression: exclude x_col and y_col if they
    # appear in controls (avoids regressing a variable on itself)
    ctrl_cols = [c for c in controls if c != x_col and c != y_col
                 and c in sub.columns]

    if not ctrl_cols:           # nothing to partial out
        rx = sub[x_col].values
        ry = sub[y_col].values
    else:
        X_ctrl = sub[ctrl_cols].values              # shape (n, k), always 2D
        def residual(col):
            y_vals = sub[col].values                # shape (n,), always 1D
            lr = LinearRegression().fit(X_ctrl, y_vals)
            return y_vals - lr.predict(X_ctrl).ravel()  # shape (n,)
        rx = residual(x_col)                        # 1D ndarray
        ry = residual(y_col)                        # 1D ndarray

    r, _ = pearsonr(rx, ry)
    return round(r, 4)


"""## 5. Estimator 1 — IPW via DoWhy
**Inverse Probability Weighting (IPW)** estimates the ATE by re-weighting each observation
by the inverse of its probability of receiving its actual treatment. This corrects for
confounding by making the weighted sample look like a randomised trial.

DoWhy provides a clean API for specifying the causal graph, identifying the estimand, and
plugging in the IPW estimator.

"""

def estimate_ipw_dowhy(pathway, df):
    """
    Estimate ATE using DoWhy's propensity-score weighting (IPW).
    Returns (ate_estimate, p_value_approx) or (nan, nan) on failure.
    """
    try:
        _, T_bin, _, Y, X_conf, _ = prepare_pathway_data(pathway, df)

        # Build a local dataframe for DoWhy
        local_df = pd.DataFrame(X_conf, columns=pathway["confounders"])
        local_df["treatment"] = T_bin
        local_df["outcome"]   = Y

        # Build causal graph string for DoWhy
        common_causes_str = " ".join(pathway["confounders"])
        gml_graph = (
            'graph[directed 1 '
            'node[id "treatment" label "treatment"] '
            'node[id "outcome" label "outcome"] '
        )
        for c in pathway["confounders"]:
            safe = c.replace(" ", "_").replace("(", "").replace(")", "").replace("%", "pct").replace("-", "_").replace("/", "_")[:30]
            gml_graph += f'node[id "{safe}" label "{safe}"] '
            gml_graph += f'edge[source "{safe}" target "treatment"] '
            gml_graph += f'edge[source "{safe}" target "outcome"] '
        gml_graph += 'edge[source "treatment" target "outcome"]]'

        # Rename confounder columns to safe names in local_df
        safe_conf_names = []
        for c in pathway["confounders"]:
            safe = c.replace(" ", "_").replace("(", "").replace(")", "").replace("%", "pct").replace("-", "_").replace("/", "_")[:30]
            safe_conf_names.append(safe)
        local_df.columns = safe_conf_names + ["treatment", "outcome"]

        model = CausalModel(
            data=local_df,
            treatment="treatment",
            outcome="outcome",
            common_causes=safe_conf_names,
            graph=gml_graph
        )

        identified = model.identify_effect(proceed_when_unidentifiable=True)

        estimate = model.estimate_effect(
            identified,
            method_name="backdoor.propensity_score_weighting",
            method_params={
                "weighting_scheme": "ips_weight",
                "propensity_score_model": LogisticRegression(max_iter=1000, C=1.0)
            }
        )

        ate = estimate.value
        # Bootstrap SE for p-value approximation
        n_boot = 200
        boot_ates = []
        for _ in range(n_boot):
            idx = np.random.choice(len(local_df), len(local_df), replace=True)
            b_df = local_df.iloc[idx].reset_index(drop=True)
            b_model = CausalModel(
                data=b_df,
                treatment="treatment",
                outcome="outcome",
                common_causes=safe_conf_names,
                graph=gml_graph
            )
            b_id = b_model.identify_effect(proceed_when_unidentifiable=True)
            b_est = b_model.estimate_effect(
                b_id,
                method_name="backdoor.propensity_score_weighting",
                method_params={
                    "weighting_scheme": "ips_weight",
                    "propensity_score_model": LogisticRegression(max_iter=1000, C=1.0)
                }
            )
            boot_ates.append(b_est.value)

        se = np.std(boot_ates)
        z = ate / se if se > 0 else np.nan
        p_val = 2 * (1 - stats.norm.cdf(abs(z))) if not np.isnan(z) else np.nan
        ci_lo = ate - 1.96 * se
        ci_hi = ate + 1.96 * se

        return {"ATE": ate, "SE": se, "CI_lo": ci_lo, "CI_hi": ci_hi, "p_value": p_val}

    except Exception as e:
        print(f"    IPW DoWhy failed: {e}")
        return {"ATE": np.nan, "SE": np.nan, "CI_lo": np.nan, "CI_hi": np.nan, "p_value": np.nan}



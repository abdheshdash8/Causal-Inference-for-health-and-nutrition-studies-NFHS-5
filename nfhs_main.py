"""### Separating Causal Pathways from Mere Correlations

A standard criticism of constraint-based discovery is that high partial correlation can produce spurious edges that merely reflect confounding rather than genuine causal direction. We apply three complementary filters to screen edges discovered in the PC algorithm on the **Total-area state-level data**:

1. **Orientation stability** (bootstrap): a directed edge $A \to B$ is retained only if the forward confidence $P_{\text{boot}}(A \to B) > \tau$ **and** it exceeds the reverse confidence $P_{\text{boot}}(B \to A)$ by a margin $\Delta$. If both directions appear with comparable frequency, the edge is bidirected and flagged as non-identifiable (your own intuition, formalized).

2. **Partial correlation residual**: after regressing out SGDP and literacy, does the conditional association persist? If the partial correlation collapses to near-zero, the edge is a confounding artifact.

3. **Asymmetry score** $\alpha = P(A\to B) - P(B\to A)$: edges with $|\alpha| < 0.15$ are flagged as orientation-ambiguous regardless of confidence.
"""


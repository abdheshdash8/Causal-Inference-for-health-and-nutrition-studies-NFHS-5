SEVERITY_KW = ["mild","moderate","severe","very high","elevated","stage","grade"]

def is_severity_pair(row):
    i1, i2 = row["Indicator_1"].lower(), row["Indicator_2"].lower()
    return any(k in i1 and k in i2 for k in SEVERITY_KW)

def is_gender_mirror(row):
    i1, i2 = row["Indicator_1"].lower(), row["Indicator_2"].lower()
    return (("men" in i1 and "women" in i2) or ("women" in i1 and "men" in i2)
            or ("male" in i1 and "female" in i2) or ("female" in i1 and "male" in i2))

POP_GROUPS = ["children under 5", "children age 12-23", "children age 6-59"]

def is_same_population(row):
    i1, i2 = row["Indicator_1"].lower(), row["Indicator_2"].lower()
    return any(p in i1 and p in i2 for p in POP_GROUPS)

def sort_pair(row):
    return tuple(sorted([row["Indicator_1"], row["Indicator_2"]]))

def apply_standard_filters(df, abs_col="Abs_Correlation", threshold=1.95):
    """Apply all redundancy filters to a pairwise correlation DataFrame."""
    df = df[~df["Indicator_1"].str.contains("Number of", case=False)]
    df = df[~df["Indicator_2"].str.contains("Number of", case=False)]
    df = df[df["Indicator_1"] != df["Indicator_2"]]
    df = df[df[abs_col] <= threshold]
    df = df[~df.apply(is_gender_mirror, axis=1)]
    df = df[~df.apply(is_severity_pair, axis=1)]
    df = df[~df.apply(is_same_population, axis=1)]
    return df.sort_values(abs_col, ascending=False)

def corr_matrix_to_pairs(mat, val_col="Correlation"):
    """Upper-triangle stack of a square correlation matrix → long DataFrame."""
    pairs = (
        mat.where(np.triu(np.ones(mat.shape), k=1).astype(bool))
        .stack().reset_index()
    )
    pairs.columns = ["Indicator_1", "Indicator_2", val_col]
    pairs["Abs_Correlation"] = pairs[val_col].abs()
    pairs["Sign"] = np.where(pairs[val_col] >= 0, "Positive", "Negative")
    return pairs


"""### Bootstrap Edge-Confidence Threshold Selection

The bootstrap threshold $\tau$ controls which edges are retained in the final DAG. A threshold too low includes spurious edges (high false-positive rate); too high discards true causal edges (high false-negative rate). We sweep $\tau$ from 0.05 to 0.50 and plot: (1) number of retained edges, (2) fraction of bidirected (ambiguous) edges, and (3) fraction of isolated nodes — looking for a stable knee point.
"""

# ── Bootstrap threshold sweep — Cluster 1 as pilot ──────────────────────
from collections import defaultdict
import numpy as np, matplotlib.pyplot as plt, pandas as pd

# Run bootstrap once, store ALL edge confidences (no threshold yet)
cluster_id_pilot = 1
cols_pilot = cluster_df[cluster_df.Cluster == cluster_id_pilot]['Indicator'].tolist()
data_pilot = df_clean[cols_pilot].dropna()
N_BOOT = 100

edge_counts_raw   = defaultdict(int)   # directed A→B
bidir_counts_raw  = defaultdict(int)   # undirected A—B (both directions seen)

for _ in range(N_BOOT):
    sample = data_pilot.sample(frac=1, replace=True)
    cg_b   = pc(sample.values)
    adj_b  = cg_b.G.graph
    for i in range(adj_b.shape[0]):
        for j in range(adj_b.shape[1]):
            if adj_b[i,j]==1 and adj_b[j,i]==-1:
                edge_counts_raw[(cols_pilot[i], cols_pilot[j])] += 1
            elif adj_b[i,j]==-1 and adj_b[j,i]==-1:   # undirected
                bidir_counts_raw[(cols_pilot[i], cols_pilot[j])] += 1

all_conf = {e: c/N_BOOT for e, c in edge_counts_raw.items()}
bidir_conf = {e: c/N_BOOT for e, c in bidir_counts_raw.items()}

thresholds = np.arange(0.05, 0.55, 0.05)
n_edges, n_bidir_frac, n_isolated = [], [], []

for tau in thresholds:
    retained   = {e: c for e, c in all_conf.items() if c >= tau}
    n_edges.append(len(retained))
    # bidir fraction = undirected edges above tau / all edges above tau
    bidir_ret  = {e: c for e, c in bidir_conf.items() if c >= tau}
    n_bidir_frac.append(len(bidir_ret) / max(len(retained), 1))
    # isolated nodes
    active = set()
    for u,v in retained:
        active.add(u); active.add(v)
    n_isolated.append(len(cols_pilot) - len(active))

fig, axes = plt.subplots(1, 3, figsize=(14, 4))
axes[0].plot(thresholds, n_edges, marker='o')
axes[0].set_xlabel('Threshold τ'); axes[0].set_ylabel('Retained edges')
axes[0].set_title('No. of retained directed edges')

axes[1].plot(thresholds, n_bidir_frac, marker='o', color='orange')
axes[1].set_xlabel('Threshold τ'); axes[1].set_ylabel('Fraction ambiguous')
axes[1].set_title('Fraction of undirected (ambiguous) edges')

axes[2].plot(thresholds, n_isolated, marker='o', color='red')
axes[2].set_xlabel('Threshold τ'); axes[2].set_ylabel('Isolated nodes')
axes[2].set_title('Nodes with no retained edge')


def is_child_to_maternal(a, b):
    """Returns True if A is a child outcome and B is a maternal input
    (implausible causal direction in a cross-sectional survey)."""
    a_is_child    = any(kw in nl(a) for kw in CHILD_KW)
    b_is_maternal = any(kw in nl(b) for kw in MATERNAL_INPUT_KW)
    return a_is_child and b_is_maternal


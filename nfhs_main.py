"""## 8. Estimator 4 — X-Learner via CausalML
The **X-Learner** improves on the T-Learner by:
1. Fitting separate outcome models (like T-Learner)
2. Computing *imputed* treatment effects for each group using the *other group's* model
3. Combining the two imputed-effect models with a propensity-score weighting

This cross-fitting makes X-Learner especially powerful when the two groups differ in size
(which is common in NFHS observational data).

"""

def estimate_x_learner(pathway, df):
    """
    Estimate ATE + CATE using CausalML BaseXLearner with GBM base models.
    """
    try:
        _, T_bin, _, Y, X_conf, _ = prepare_pathway_data(pathway, df)

        scaler = StandardScaler()
        X_scaled = scaler.fit_transform(X_conf)

        learner = BaseXLearner(
            learner=GradientBoostingRegressor(n_estimators=100, max_depth=3, random_state=42),
            control_outcome_learner=GradientBoostingRegressor(n_estimators=100, max_depth=3, random_state=42),
            treatment_outcome_learner=GradientBoostingRegressor(n_estimators=100, max_depth=3, random_state=42),
            control_effect_learner=GradientBoostingRegressor(n_estimators=100, max_depth=3, random_state=42),
            treatment_effect_learner=GradientBoostingRegressor(n_estimators=100, max_depth=3, random_state=42),
        )
        learner.fit(X_scaled, T_bin, Y)
        cate = learner.predict(X_scaled).flatten()

        ate = np.mean(cate)

        # Bootstrap CI
        n_boot = 200
        boot_ates = []
        for _ in range(n_boot):
            idx = np.random.choice(len(Y), len(Y), replace=True)
            X_b, T_b, Y_b = X_scaled[idx], T_bin[idx], Y[idx]
            if len(np.unique(T_b)) < 2:
                continue
            try:
                lb = BaseXLearner(
                    learner=GradientBoostingRegressor(n_estimators=50, max_depth=3, random_state=42),
                    control_outcome_learner=GradientBoostingRegressor(n_estimators=50, max_depth=3, random_state=42),
                    treatment_outcome_learner=GradientBoostingRegressor(n_estimators=50, max_depth=3, random_state=42),
                    control_effect_learner=GradientBoostingRegressor(n_estimators=50, max_depth=3, random_state=42),
                    treatment_effect_learner=GradientBoostingRegressor(n_estimators=50, max_depth=3, random_state=42),
                )
                lb.fit(X_b, T_b, Y_b)
                boot_ates.append(np.mean(lb.predict(X_scaled).flatten()))
            except Exception:
                continue

        se = np.std(boot_ates) if boot_ates else np.nan
        ci_lo = ate - 1.96 * se
        ci_hi = ate + 1.96 * se
        z = ate / se if se > 0 else np.nan
        p_val = 2 * (1 - stats.norm.cdf(abs(z))) if not np.isnan(z) else np.nan

        return {
            "ATE": ate, "SE": se, "CI_lo": ci_lo, "CI_hi": ci_hi,
            "p_value": p_val, "CATE": cate
        }

    except Exception as e:
        print(f"    X-Learner failed: {e}")
        return {"ATE": np.nan, "SE": np.nan, "CI_lo": np.nan, "CI_hi": np.nan,
                "p_value": np.nan, "CATE": None}



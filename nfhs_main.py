"""## Three-Criterion Causal Screening

After the bootstrap PC run (cell above) we have `screening_df` which recorded,
for every candidate directed edge $(A \to B)$ that appeared in $\ge \tau$ fraction
of bootstrap resamples:

| Column | Meaning |
|--------|---------|
| `P_fwd` | $\hat P(A\to B)$ — fraction of boots where the edge is oriented $A\to B$ |
| `P_rev` | $\hat P(B\to A)$ — fraction of boots where the edge is oriented $B\to A$ |
| `P_bidir` | fraction of boots where the edge is **undirected** ($A - B$) |
| `Asymmetry` | $\alpha_{AB} = \hat P(A\to B) - \hat P(B\to A)$ |
| `Partial_r` | Partial correlation $\rho_{AB \cdot Z}$ after removing SGDP and literacy |

An edge is classified **CAUSAL** only if it passes **all three** of the following criteria:

1. **Directional asymmetry** $\alpha_{AB} \ge \Delta = 0.10$ — the forward direction is
   clearly more frequent than the reverse; orientation is not random.
2. **Undirected-edge fraction** $P_{\text{bidir}} < 0.30$ — the edge is genuinely oriented
   in the majority of bootstrap runs; not predominantly undirected.
3. **Partial-correlation residual** $|\rho_{AB \cdot Z}| \ge 0.35$ — the association
   survives after regressing out known confounders (SGDP + literacy); not a confounding artifact.

The cells below: (a) display the full screening table, (b) show the criterion-by-criterion
pass/fail breakdown, (c) plot the asymmetry distribution, and (d) **automatically construct
`CAUSAL_PATHWAYS`** — the list of pathways fed into ATE estimation — from the passing edges,
augmented with domain-knowledge confounders.
"""


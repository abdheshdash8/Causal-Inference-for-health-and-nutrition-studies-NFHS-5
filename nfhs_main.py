# ── Helper: KDE trough detection ─────────────────────────────────────────────
def kde_trough(data, x_range, bw=None):
    """Return x-value of the deepest trough in the KDE of data."""
    data = np.array(data)
    data = data[~np.isnan(data)]
    if len(data) < 5:
        return None
    kde = gaussian_kde(data, bw_method=bw if bw else 'scott')
    xs  = np.linspace(x_range[0], x_range[1], 500)
    ys  = kde(xs)
    # Local minima
    troughs = argrelmin(ys, order=10)[0]
    if len(troughs) == 0:
        return None
    # Return the trough with the lowest KDE density (deepest gap)
    best = troughs[np.argmin(ys[troughs])]
    return float(xs[best])

# ── Helper: Jenks natural breaks (1D, k=2) ────────────────────────────────────
def jenks2(data):
    """Find the single best breakpoint separating data into 2 natural classes."""
    data = np.sort(np.array(data)[~np.isnan(data)])
    if len(data) < 4:
        return None
    best_break, best_gvf = None, -np.inf
    total_sdam = np.sum((data - data.mean())**2)
    if total_sdam == 0:
        return None
    for k in range(1, len(data)):
        lo, hi = data[:k], data[k:]
        sdcm = (np.sum((lo - lo.mean())**2) if len(lo)>1 else 0) +                (np.sum((hi - hi.mean())**2) if len(hi)>1 else 0)
        gvf  = (total_sdam - sdcm) / total_sdam
        if gvf > best_gvf:
            best_gvf   = gvf
            best_break = float(data[k])
    return best_break


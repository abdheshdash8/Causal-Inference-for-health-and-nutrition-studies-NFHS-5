# Lowercase normalisation helper
def nl(s): return s.lower().strip()

# ── Rule 1: Gender mirrors ────────────────────────────────────────────────────
# Remove if A and B are the same concept differing only in "men"↔"women" or
# "male"↔"female". We compare the token-stripped versions.
def strip_gender(s):
    s2 = re.sub(r'\b(women|men|male|female|woman|man)\b', '', nl(s))
    s2 = re.sub(r'\s+', ' ', s2).strip()
    return s2

def is_gender_mirror(a, b):
    sa, sb = strip_gender(a), strip_gender(b)
    if sa == sb:
        return True
    # Also check: one contains 'men' and other 'women' and differ only in that
    has_female_a = bool(re.search(r'\b(women|female|woman)\b', nl(a)))
    has_male_a   = bool(re.search(r'\b(men|male|man)\b', nl(a)) and
                        not re.search(r'\b(women|female)\b', nl(a)))
    has_female_b = bool(re.search(r'\b(women|female|woman)\b', nl(b)))
    has_male_b   = bool(re.search(r'\b(men|male|man)\b', nl(b)) and
                        not re.search(r'\b(women|female)\b', nl(b)))
    if (has_female_a and has_male_b) or (has_male_a and has_female_b):
        # Check that the rest of the string is similar
        if len(sa) > 5 and len(sb) > 5 and (
            sa[:20] == sb[:20] or
            abs(len(sa) - len(sb)) < 15
        ):
            return True
    return False

# ── Rule 2: Severity / sub-scale pairs ───────────────────────────────────────
# Remove if A and B are both about the same condition but different severity levels
SEVERITY_KW = ['mild', 'moderate', 'severe', 'very high', 'elevated', 'stage',
                'grade', 'high ', 'low ', 'borderline']

def base_concept(s):
    """Remove severity qualifiers and numeric cuts to get the base concept."""
    s2 = nl(s)
    for kw in SEVERITY_KW:
        s2 = s2.replace(kw, '')
    s2 = re.sub(r'\b(>|<|=|\d+\.?\d*\s*(g/dl|mm|%)?)\b', '', s2)
    s2 = re.sub(r'\s+', ' ', s2).strip()
    return s2

def is_severity_pair(a, b):
    ba, bb = base_concept(a), base_concept(b)
    if ba == bb and len(ba) > 10:
        return True
    # direct keyword overlap: both contain a severity word
    has_sev_a = any(kw in nl(a) for kw in SEVERITY_KW)
    has_sev_b = any(kw in nl(b) for kw in SEVERITY_KW)
    if has_sev_a and has_sev_b:
        # Check that the core topic is the same (long common substring)
        words_a = set(nl(a).split())
        words_b = set(nl(b).split())
        overlap = words_a & words_b
        if len(overlap) >= max(4, min(len(words_a), len(words_b)) * 0.5):
            return True
    return False

# ── Rule 3: Count / demographic / administrative variables ────────────────────
# These carry no causal mechanism for health outcomes.
COUNT_KW = [
    'number of households', 'number of women', 'number of men',
    'number of eligible', 'total number', 'population below age',
    'population living', 'households surveyed', 'women interviewed',
    'men interviewed', 'sample size', 'per 1000 population',
    'sex ratio at birth',           # biological fixed ratio, not an intervention
    'percentage of women who do not have',  # undefined double-negative
]
# Also flag purely demographic ratios
DEMO_PATTERNS = [
    r'number of \w+ (surveyed|interviewed|eligible)',
    r'population (below|above|under|over) age \d+',
    r'\bsex ratio\b',
]

def is_count_variable(s):
    sl = nl(s)
    if any(kw in sl for kw in COUNT_KW):
        return True
    if any(re.search(p, sl) for p in DEMO_PATTERNS):
        return True
    # "Number of " at start
    if sl.startswith('number of '):
        return True
    return False

def is_count_pair(a, b):
    return is_count_variable(a) or is_count_variable(b)

# ── Rule 4: Same-population / age-band sub-indicators ────────────────────────
# e.g. "Children 6-59 months anaemic" → "Children 6-23 months anaemic"
# These are the same outcome measured on a subset.
def age_band_base(s):
    """Remove age-band qualifiers."""
    s2 = re.sub(r'age \d+[-–]\d+\s*(months?|years?)', 'AGE', nl(s))
    s2 = re.sub(r'(under|below|above|over)\s+age\s+\d+', 'AGE', s2)
    s2 = re.sub(r'children\s+AGE', 'children AGE', s2)
    s2 = re.sub(r'\s+', ' ', s2).strip()
    return s2

def is_age_band_pair(a, b):
    ba, bb = age_band_base(a), age_band_base(b)
    # Same topic, different age bands
    if ba == bb and 'AGE' in ba:
        return True
    return False

# ── Rule 5: Method-mix sub-indicators of family planning ─────────────────────
# "FP - Pill", "FP - Condom", "FP - Sterilisation" → these are sub-categories
# of the same FP total; one causes the other only through the FP total.

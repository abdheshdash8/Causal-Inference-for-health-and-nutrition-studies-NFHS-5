"""## 11. CATE Heterogeneity — State-Level Treatment Effect Distribution

The DR Learner and X-Learner both produce **Conditional Average Treatment Effects (CATEs)**,
i.e., treatment effects that vary across states. We visualise the distribution of CATEs
to understand *where* the treatment effect is large vs. small.

"""

def plot_cate_distributions(pathway_list, dr_res, x_res, df, figsize=(14, 18)):
    """
    Side-by-side violin plots of state-level CATEs from DR and X learners.
    """
    n_valid = sum(
        1 for p in pathway_list
        if dr_res.get(p["label"], {}).get("CATE") is not None
        or x_res.get(p["label"],  {}).get("CATE") is not None
    )

    if n_valid == 0:
        print("No CATE data available for plotting.")
        return

    fig, axes = plt.subplots(
        n_valid, 1, figsize=figsize, squeeze=False
    )

    ax_idx = 0
    for p in pathway_list:
        label = p["label"]
        dr_cate = dr_res.get(label, {}).get("CATE")

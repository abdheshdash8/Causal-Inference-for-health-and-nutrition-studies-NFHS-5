"""## 4. Data Preparation Helper"""

def prepare_pathway_data(pathway, base_df, min_rows=5):
    """
    Extract, clean, and binarise treatment for a single pathway.
    Returns (df_ready, T_binary, T_cont, Y, X_conf) or raises ValueError.
    """
    cols_needed = (
        [pathway["treatment"], pathway["outcome"]] + pathway["confounders"]
    )
    # Keep only columns that exist in the dataframe
    cols_present = [c for c in cols_needed if c in base_df.columns]
    missing = set(cols_needed) - set(cols_present)
    if missing:
        raise ValueError(f"Missing columns: {missing}")

    df = base_df[cols_present].copy()
    df = df.replace("*", np.nan)
    for c in cols_present:
        df[c] = pd.to_numeric(df[c], errors="coerce")
    df = df.dropna()

    if len(df) < min_rows:
        raise ValueError(
            f"Only {len(df)} rows after cleaning (need {min_rows})."
        )

    T_cont = df[pathway["treatment"]].values
    Y      = df[pathway["outcome"]].values
    X_conf = df[pathway["confounders"]].values

    # Binary treatment: above median = treated
    threshold = np.median(T_cont)
    T_binary  = (T_cont >= threshold).astype(int)

    return df, T_binary, T_cont, Y, X_conf, threshold



print("\n DR Learner estimation complete.")

"""## 7. Estimator 3 — T-Learner via CausalML
The **T-Learner** (Two-model learner) fits *separate* outcome models for the treated group
(T=1) and the control group (T=0). The CATE for each unit is the difference in predictions:
CATE(x) = μ₁(x) − μ₀(x).

This is the most intuitive meta-learner but can suffer from high variance when treated/control
subsamples are small (which is why we also run X-Learner below).

"""

def estimate_t_learner(pathway, df):
    """
    Estimate ATE + CATE using CausalML BaseTLearner with GBM base models.
    """
    try:
        _, T_bin, _, Y, X_conf, _ = prepare_pathway_data(pathway, df)

        scaler = StandardScaler()
        X_scaled = scaler.fit_transform(X_conf)

        learner = BaseTLearner(
            learner=GradientBoostingRegressor(n_estimators=100, max_depth=3, random_state=42)
        )
        learner.fit(X_scaled, T_bin, Y)
        cate = learner.predict(X_scaled).flatten()

        ate = np.mean(cate)

        # Bootstrap CI
        n_boot = 200
        boot_ates = []
        for _ in range(n_boot):
            idx = np.random.choice(len(Y), len(Y), replace=True)
            X_b, T_b, Y_b = X_scaled[idx], T_bin[idx], Y[idx]
            if len(np.unique(T_b)) < 2:
                continue
            lb = BaseTLearner(
                learner=GradientBoostingRegressor(n_estimators=50, max_depth=3, random_state=42)
            )
            lb.fit(X_b, T_b, Y_b)
            boot_ates.append(np.mean(lb.predict(X_scaled).flatten()))

        se = np.std(boot_ates) if boot_ates else np.nan
        ci_lo = ate - 1.96 * se
        ci_hi = ate + 1.96 * se
        z = ate / se if se > 0 else np.nan
        p_val = 2 * (1 - stats.norm.cdf(abs(z))) if not np.isnan(z) else np.nan

        return {
            "ATE": ate, "SE": se, "CI_lo": ci_lo, "CI_hi": ci_hi,
            "p_value": p_val, "CATE": cate
        }

    except Exception as e:
        print(f"    T-Learner failed: {e}")
        return {"ATE": np.nan, "SE": np.nan, "CI_lo": np.nan, "CI_hi": np.nan,
                "p_value": np.nan, "CATE": None}


